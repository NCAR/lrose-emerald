%Default colormap for reflectivity values

function x = dbm_default(n);


if nargin==1 & isempty(n)
    n = size(get(gcf,'Colormap'),1);
end;

cmap = [...
  30,144,255;...
  65,105,225;...
  0,0,255;...
  0,0,205;...
  0,0,128;...
  25,25,112;...
  112,128,144;...
  34,139,34;...
  154,205,50;...
  205,173,0;...
  255,215,0;...
  255,255,0;...
  255,165,0;...
  255,127,80;...
  255,99,71;...
  255,100,0;...
  255,69,0;...
  255,48,48;...
  255,0,0;...
  255,10,80;...
  255,20,147;...
  255,52,179;...
  255,105,180;...
  255,150,180;...
  255,192,203; ...
  255,200,230 ...
    ];

cmap=cmap./255;

if nargin < 1
    n = size(cmap,1);
end;

x = interp1(linspace(0,1,size(cmap,1)),cmap(:,1),linspace(0,1,n)','linear');
x(:,2) = interp1(linspace(0,1,size(cmap,1)),cmap(:,2),linspace(0,1,n)','linear');
x(:,3) = interp1(linspace(0,1,size(cmap,1)),cmap(:,3),linspace(0,1,n)','linear');

x = min(x,1);
x = max(x,0);